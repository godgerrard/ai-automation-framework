# ── URL Safety Helper ─────────────────────────────────────────────────────────

def is_safe_probe_url(url: str) -> tuple[bool, str]:
    """Return (True, "") if url is safe to hand to a browser/probe, else (False, reason).

    Allows only http/https. Rejects file:, javascript:, data:, ftp:, gopher:, about:,
    blank, non-string, and over-long (>2048) inputs.

    NOTE: localhost and RFC-1918 private IP addresses are intentionally allowed —
    testing apps on localhost or internal networks is a primary use case of this
    framework. Do NOT add blocks for 127.x, 192.168.x, 10.x, etc.
    """
    if not isinstance(url, str):
        return False, "URL must be a string"
    if not url or not url.strip():
        return False, "URL must not be empty"
    if len(url) > 2048:
        return False, "URL exceeds maximum allowed length of 2048 characters"
    try:
        parsed = urlparse(url)
    except Exception as exc:
        return False, f"URL could not be parsed: {exc}"
    if parsed.scheme not in {"http", "https"}:
        return False, f"Scheme {parsed.scheme!r} is not allowed; only http and https are permitted"
    if not parsed.netloc:
        return False, "URL has no host/netloc component"
    return True, ""


# ── Story Parser ──────────────────────────────────────────────────────────────

class StoryParser:
    """Reads user story files (.json or .txt/.md) into a normalized dict."""

    REQUIRED_FIELDS = {"id", "title", "steps"}

    def parse_story_file(self, path: str) -> Dict[str, Any]:
        file_path = Path(path)
        if not file_path.exists():
            raise FileNotFoundError(f"Story file not found: {path}")

        suffix = file_path.suffix.lower()
        if suffix == ".json":
            return self._parse_json(file_path)
        if suffix in (".txt", ".md"):
            return self._parse_text(file_path)
        raise ValueError(f"Unsupported story file format: {suffix!r}")

    def _parse_json(self, path: Path) -> Dict[str, Any]:
        with open(path, encoding="utf-8") as f:
            data = json.load(f)
        self._validate(data, path)
        return data

    def _parse_text(self, path: Path) -> Dict[str, Any]:
        lines = [ln.strip() for ln in path.read_text(encoding="utf-8").splitlines() if ln.strip()]
        story: Dict[str, Any] = {
            "id": path.stem,
            "title": lines[0] if lines else "Untitled Story",
            "description": "",
            "steps": [],
            "tags": [],
        }
        counter = 1
        for line in lines[1:]:
            low = line.lower()
            if low.startswith(("given ", "when ", "then ", "and ")):
                _, _, desc = line.partition(" ")
                story["steps"].append({
                    "id": f"step_{counter:02d}",
                    "action": _infer_action(desc),
                    "target": "",
                    "value": "",
                    "description": line,
                })
                counter += 1
            elif not story["description"]:
                story["description"] = line
        return story

    def _validate(self, data: Dict, path: Path) -> None:
        missing = self.REQUIRED_FIELDS - set(data)
        if missing:
            raise ValueError(f"{path.name}: missing required fields {missing}")


