        story_id = story.get("id", "generated")
        title = story.get("title", "Generated Test")
        description = story.get("description", "")
        steps = story.get("steps", [])
        tags = story.get("tags", [])
        neg_scenarios = story.get("negative_scenarios", [])
        ts = _timestamp()

        lines: List[str] = []

