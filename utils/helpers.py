class ProjectScaffolder:
    """Scaffold a complete test project skeleton for a new target application.

    Generated files are listed in .gitignore — the framework repo stays
    application-agnostic.  Each client adapts the skeleton to their app's DOM/API.

    Usage:
        scaffolder = ProjectScaffolder("https://myapp.com", "myapp", "both")
        scaffolder.scaffold()
    """

    def __init__(self, url: str, name: str, project_type: str = "both") -> None:
        self.url = url.rstrip("/")
        self.name = re.sub(r"[\s\-]+", "_", name.strip().lower())
        self.project_type = project_type
        self.class_name = "".join(w.capitalize() for w in self.name.split("_") if w)

    def scaffold(self) -> list[str]:
        created: list[str] = []
        if self.project_type in ("web", "both"):
            created += self._scaffold_web()
        if self.project_type in ("api", "both"):
            created += self._scaffold_api()
        return created

    # ── Web skeleton ──────────────────────────────────────────────────────────

    def _scaffold_web(self) -> list[str]:
        created = []
        locators_path = Path("locators") / f"{self.name}_locators.py"
        page_path = Path("pages") / f"{self.name}_page.py"
        story_path = Path("stories") / f"{self.name}_story.json"
        test_path = Path("tests") / "workflows" / f"test_{self.name}.py"

        for p in (locators_path, page_path, test_path):
            p.parent.mkdir(parents=True, exist_ok=True)
        story_path.parent.mkdir(parents=True, exist_ok=True)

        if not locators_path.exists():
            locators_path.write_text(self._locators_source(), encoding="utf-8")
            created.append(str(locators_path))

        if not page_path.exists():
            page_path.write_text(self._page_source(), encoding="utf-8")
            created.append(str(page_path))

        if not story_path.exists():
            story_path.write_text(self._web_story_json(), encoding="utf-8")
            created.append(str(story_path))

        if not test_path.exists():
            test_path.write_text(self._web_test_source(), encoding="utf-8")
            created.append(str(test_path))

        return created

