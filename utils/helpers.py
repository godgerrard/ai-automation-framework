    def _scaffold_api(self) -> list[str]:
        created = []
        story_path = Path("stories") / f"{self.name}_api_story.json"
        test_path = Path("tests") / "api" / f"test_{self.name}_api.py"

        story_path.parent.mkdir(parents=True, exist_ok=True)
        test_path.parent.mkdir(parents=True, exist_ok=True)

        if not story_path.exists():
            story_path.write_text(self._api_story_json(), encoding="utf-8")
            created.append(str(story_path))

        if not test_path.exists():
            test_path.write_text(self._api_test_source(), encoding="utf-8")
            created.append(str(test_path))

        return created

