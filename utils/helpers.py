class APICodeGenerator:
    """Generates pytest API test classes from API story dicts."""

    def generate_test_class(self, story: Dict[str, Any]) -> str:
        story_id = story.get("id", "generated")
        title = story.get("title", "Generated API Test")
        description = story.get("description", "")
        tags = story.get("tags", [])
        endpoints = story.get("endpoints", [])
        neg_scenarios = story.get("negative_scenarios", [])
        base_url = story.get("base_url", "")
        ts = _timestamp()

        lines: List[str] = []

        lines += [
            '"""',
            f"Generated by AI Automation Framework  |  {ts}",
            f"Story : {title}",
            f"ID    : {story_id}",
            f'Target: {base_url}',
            '"""',
            "from __future__ import annotations",
            "",
            "import pytest",
            "",
            "from services.api_service import APIService",
            "",
            "",
            f"class Test{_to_class_name(story_id)}:",
            f'    """{title}',
            "",
            f"    {description}",
            '    """',
            "",
        ]

        marks = "".join(f"    @pytest.mark.{tag}\n" for tag in tags)

        for endpoint in endpoints:
            ep_id = _slug(endpoint.get("id", "endpoint"))
            method = endpoint.get("method", "GET").lower()
            path = endpoint.get("path", "/")
            body = endpoint.get("body")
            params = endpoint.get("params")
            expected_status = endpoint.get("expected_status", 200)
            expected_schema = endpoint.get("expected_schema", [])
            desc = endpoint.get("description", f"{method.upper()} {path}")

            lines.append(marks.rstrip())
            lines.append(f"    def test_{ep_id}(self, api_client: APIService) -> None:")
            lines.append(f'        """{desc}"""')

            call = _api_call_code(method, path, body, params)
            lines.append(f"        resp = {call}")

            chain = f"        resp.assert_status({expected_status})"
            if expected_schema:
                keys = ", ".join(f'"{k}"' for k in expected_schema)
                chain += f".assert_json_key_exists({keys})"
            lines.append(chain)
            lines.append("")

        for scenario in neg_scenarios:
            name = _slug(scenario.get("id", "negative"))
            scenario_title = scenario.get("title", "")
            expected_status = scenario.get("expected_status", 400)
            method = scenario.get("method", "GET").lower()
            path = scenario.get("path", "/")
            body = scenario.get("body")
            expected = scenario.get("expected", "")

            lines += [
                marks.rstrip(),
                f"    def test_{name}(self, api_client: APIService) -> None:",
                f'        """{scenario_title}',
                "",
                f"        Expected: {expected}",
                '        """',
                f"        resp = {_api_call_code(method, path, body, None)}",
                f"        resp.assert_status({expected_status})",
                "",
            ]

        return "\n".join(lines)


def _api_call_code(
    method: str,
    path: str,
    body: Any,
    params: Any,
) -> str:
    if method == "get":
        if params:
            return f'api_client.get("{path}", params={params!r})'
        return f'api_client.get("{path}")'
    if method == "post":
        return f'api_client.post("{path}", {body!r})'
    if method == "put":
        return f'api_client.put("{path}", {body!r})'
    if method == "patch":
        return f'api_client.patch("{path}", {body!r})'
    if method == "delete":
        return f'api_client.delete("{path}")'
    return f'api_client.get("{path}")  # TODO: unknown method {method!r}'


# ── Module-level helpers ──────────────────────────────────────────────────────

def _infer_action(description: str) -> str:
    low = description.lower()
    if any(k in low for k in ("click", "press", "tap", "submit", "button")):
        return "click"
    if any(k in low for k in ("type", "enter", "fill", "input", "write")):
        return "fill"
    if any(k in low for k in ("navigate", "go to", "open", "visit", "load")):
        return "navigate"
    if any(k in low for k in ("url", "redirect", "route")):
        return "assert_url"
    return "assert_visible"


def _action_to_code(action: str, target: str, value: str, desc: str) -> str:
    comment = f"  # {desc}" if desc else ""
    if action == "navigate":
        return f'page.goto(base_url + "{target}"){comment}'
    if action == "fill":
        return f'page.fill("{target}", "{value}"){comment}'
    if action == "click":
        return f'page.click("{target}"){comment}'
    if action == "assert_url":
        return f'page.wait_for_url("**{target}"){comment}'
    if action == "assert_visible":
        return f'assert page.locator("{target}").is_visible(){comment}'
    if action == "assert_text":
        return f'assert "{value}" in page.locator("{target}").inner_text(){comment}'
    if action == "select":
        return f'page.select_option("{target}", "{value}"){comment}'
    return f'# TODO: implement step — {action} on "{target}"{comment}'


def _camel_to_snake(name: str) -> str:
    s = re.sub(r"(.)([A-Z][a-z]+)", r"\1_\2", name)
    return re.sub(r"([a-z0-9])([A-Z])", r"\1_\2", s).lower()


def _to_class_name(slug: str) -> str:
    return "".join(w.capitalize() for w in re.split(r"[\W_]+", slug) if w)


def _slug(text: str) -> str:
    return re.sub(r"\W+", "_", text.lower()).strip("_")


def _url_path(url: str) -> str:
    from urllib.parse import urlparse
    return urlparse(url).path or "/"


def _timestamp() -> str:
    return datetime.now().isoformat(timespec="seconds")


